#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	uc;

	if (c == -1)
		return (0);
	uc = (unsigned char)c;
	return ((uc >= 'A' && uc <= 'Z') || (uc >= 'a' && uc <= 'z'));
}
